package com.atomgraph.linkeddatahub.vocabulary;

import org.apache.jena.ontology.ObjectProperty;
import org.apache.jena.ontology.OntModel;
import org.apache.jena.ontology.OntModelSpec;
import org.apache.jena.rdf.model.ModelFactory;

/**
 *
 * @author Martynas Jusevičius <martynas@atomgraph.com>
 */
public class LMOD
{

    /** <p>The RDF model that holds the vocabulary terms</p> */
    private static OntModel m_model = ModelFactory.createOntologyModel(OntModelSpec.OWL_MEM, null);
    
    /** <p>The namespace of the vocabulary as a string</p> */
    public static final String NS = "https://w3id.org/atomgraph/linkeddatahub/admin/modules/domain#";
    
    /** <p>The namespace of the vocabulary as a string</p>
     *  @see #NS */
    public static String getURI()
    {
        return NS;
    }
    
    public static final ObjectProperty endUserDataset = m_model.createObjectProperty( NS + "endUserDataset" );

    public static final ObjectProperty adminDataset = m_model.createObjectProperty( NS + "adminDataset" );
    
}
